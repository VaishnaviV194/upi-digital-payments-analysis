-- 6. KEY BUSINESS INSIGHTS

-- 1. DATA QUALITY IS HIGH OUT OF THE BOX: 0 NULLS, 0 DUPLICATE
--    TRANSACTION IDS, 0 NEGATIVE/ZERO AMOUNTS, AND NO INVALID
--    HOUR_OF_DAY OR FRAUD_FLAG VALUES — Q11-Q18 ALL RETURN 0 ROWS.
-- 2. SBI DOMINATES ON BOTH VOLUME AND VALUE — IT LEADS EVERY BANK BY
--    TRANSACTION COUNT AND CONTRIBUTES ~₹82.8M IN TOTAL VALUE, ROUGHLY
--    ₹33M MORE THAN THE #2 BANK (HDFC, ~₹49.8M).
-- 3. BANK SUCCESS RATES ARE TIGHTLY CLUSTERED (94.9%-95.2%), SO
--    RELIABILITY ISN'T REALLY A DIFFERENTIATOR BETWEEN BANKS — THE
--    GAP BETWEEN THE BEST (HDFC) AND WORST (YES BANK) IS UNDER HALF A
--    PERCENTAGE POINT.
-- 4. SHOPPING IS THE BIGGEST VALUE DRIVER OVERALL (~₹76.9M TOTAL),
--    BUT EDUCATION HAS THE HIGHEST AVERAGE TICKET SIZE PER
--    TRANSACTION — HIGH-FREQUENCY CATEGORIES AND HIGH-VALUE
--    CATEGORIES ARE NOT THE SAME CATEGORIES.
-- 5. FRAUD IS RARE (480 OF 250,000 TRANSACTIONS, ~0.19%) AND SLIGHTLY
--    CONCENTRATED: KARNATAKA AND RAJASTHAN HAVE THE HIGHEST STATE-LEVEL
--    FRAUD RATES (~0.23%), AND TRANSPORT/EDUCATION HAVE THE HIGHEST
--    CATEGORY-LEVEL FRAUD RATES (~0.21%) DESPITE NOT BEING THE
--    HIGHEST-VALUE CATEGORIES.
-- 6. SPENDING PEAKS IN THE 36-45 AGE GROUP (~₹1,424 AVG/TXN) AND
--    DECLINES STEADILY FOR BOTH YOUNGER (18-25: ~₹1,195) AND OLDER
--    (56+: ~₹1,188) SENDERS.
-- 7. THE EVENING HOURS (18:00-20:00) DOMINATE BOTH TRANSACTION VOLUME
--    AND TOTAL VALUE, WITH HOUR 19 ALONE CONTRIBUTING ~₹28.2M — CAPACITY
--    AND FRAUD MONITORING SHOULD WEIGHT THIS WINDOW MORE HEAVILY.
-- 8. WEEKDAY VS WEEKEND BEHAVIOR IS NEARLY FLAT ON A PER-TRANSACTION
--    BASIS (AVG ~₹1,312 VS ~₹1,310) — WEEKENDS BRING LOWER VOLUME, NOT
--    MATERIALLY DIFFERENT SPENDING HABITS.


-- 7. BUSINESS RECOMMENDATIONS

-- 1. FOCUS RELIABILITY ENGINEERING ON CLOSING THE SMALL SUCCESS-RATE
--    GAP AT YES BANK/ICICI RATHER THAN TREATING IT AS A MAJOR OUTAGE
--    RISK — THE ABSOLUTE GAP VERSUS THE BEST PERFORMER IS SMALL BUT
--    CONSISTENT ENOUGH TO BE WORTH INVESTIGATING.
-- 2. SCALE INFRASTRUCTURE AND FRAUD-REVIEW STAFFING FOR THE
--    18:00-20:00 WINDOW SPECIFICALLY, SINCE IT'S THE SINGLE HEAVIEST
--    PERIOD FOR BOTH VOLUME AND RUPEE VALUE.
-- 3. APPLY TIGHTER FRAUD-DETECTION THRESHOLDS TO EDUCATION AND
--    TRANSPORT TRANSACTIONS — THEY COMBINE ABOVE-AVERAGE FRAUD RATES
--    WITH MEANINGFUL TRANSACTION VALUES, UNLIKE LOWER-FRAUD,
--    HIGH-VALUE CATEGORIES LIKE UTILITIES.
-- 4. RUN STATE-SPECIFIC FRAUD MONITORING FOR KARNATAKA AND RAJASTHAN
--    GIVEN THEIR CONSISTENTLY ELEVATED FRAUD RATES RELATIVE TO OTHER
--    HIGH-VOLUME STATES.
-- 5. DESIGN RETENTION OFFERS AROUND THE 36-45 AGE SEGMENT (HIGHEST
--    AVERAGE SPEND) WHILE USING ACQUISITION-STYLE INCENTIVES FOR THE
--    18-25 AND 56+ SEGMENTS, WHERE AVERAGE SPEND IS LOWER.
-- 6. TREAT WEEKEND DIPS AS A VOLUME PROBLEM, NOT A BEHAVIOR-CHANGE
--    PROBLEM — SINCE PER-TRANSACTION VALUE BARELY MOVES, WEEKEND
--    PROMOTIONS SHOULD AIM TO DRIVE MORE TRANSACTIONS RATHER THAN
--    BIGGER ONES.



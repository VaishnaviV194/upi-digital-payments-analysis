-- Project: UPI Digital Payments Analysis
-- Tools: MySQL
-- Dataset Size: 2,50,000 Transactions
-- Objective: Analyze UPI digital payment transactions to understand transaction patterns, bank performance, fraud trends, and customer spending behavior.


-- 1. DATABASE SETUP

CREATE DATABASE UPI_PAYMENTS_ANALYSIS;
USE UPI_PAYMENTS_ANALYSIS;


-- 2. DATA EXPLORATION 

-- Q1. PREVIEW THE DATASET
SELECT * FROM UPI_TRANSACTIONS LIMIT 10;

-- Q2. TOTAL NUMBER OF TRANSACTIONS
SELECT COUNT(*) AS TOTAL_TRANSACTIONS FROM UPI_TRANSACTIONS;

-- Q3. TOTAL NUMBER OF COLUMNS
SELECT COUNT(*) AS TOTAL_COLUMNS
FROM INFORMATION_SCHEMA.COLUMNS
WHERE TABLE_SCHEMA = 'UPI_PAYMENTS_ANALYSIS'
  AND TABLE_NAME = 'UPI_TRANSACTIONS';

-- Q4. DATASET TIME PERIOD
SELECT MIN(`TIMESTAMP`) AS START_DATE,
       MAX(`TIMESTAMP`) AS END_DATE,
       DATEDIFF(MAX(`TIMESTAMP`), MIN(`TIMESTAMP`)) AS DAYS_COVERED
FROM UPI_TRANSACTIONS;

-- Q5. DISTINCT SENDER BANKS
SELECT COUNT(DISTINCT sender_bank) AS TOTAL_SENDER_BANKS FROM UPI_TRANSACTIONS;

-- Q6. DISTINCT RECEIVER BANKS
SELECT COUNT(DISTINCT RECEIVER_bank) AS TOTAL_RECEIVER_BANKS FROM UPI_TRANSACTIONS;

-- Q7. DISTINCT MERCHANT CATEGORIES
SELECT COUNT(DISTINCT MERCHANT_CATEGORY) AS TOTAL_MERCHANT_CATEGORY FROM UPI_TRANSACTIONS;

-- Q8. DISTINCT STATES
SELECT COUNT(DISTINCT sender_STATE) AS TOTAL_SENDER_STATE FROM UPI_TRANSACTIONS;

-- Q9. TRANSACTION STATUS DISTRIBUTION
SELECT TRANSACTION_STATUS,
       COUNT(*) AS TXN_COUNT,
       ROUND(COUNT(*) * 100.0 / (SELECT COUNT(*) FROM UPI_TRANSACTIONS), 2) AS PCT
FROM UPI_TRANSACTIONS
GROUP BY TRANSACTION_STATUS;

-- Q10. FRAUD VS NON-FRAUD TRANSACTIONS
SELECT CASE WHEN FRAUD_FLAG = 1 THEN 'FRAUD' ELSE 'NON-FRAUD' END AS TXN_CLASS,
       COUNT(*) AS TXN_COUNT,
       ROUND(COUNT(*) * 100.0 / (SELECT COUNT(*) FROM UPI_TRANSACTIONS), 4) AS PCT
FROM UPI_TRANSACTIONS
GROUP BY TXN_CLASS;


-- 3. DATA QUALITY ASSESSMENT & CLEANING 

-- Q11. CHECK NULL VALUES 
SELECT
    SUM(`TRANSACTION ID` IS NULL)          AS NULL_TXN_ID,
    SUM(`TIMESTAMP` IS NULL)               AS NULL_TIMESTAMP,
    SUM(`TRANSACTION TYPE` IS NULL)        AS NULL_TXN_TYPE,
    SUM(MERCHANT_CATEGORY IS NULL)         AS NULL_CATEGORY,
    SUM(`AMOUNT (INR)` IS NULL)            AS NULL_AMOUNT,
    SUM(TRANSACTION_STATUS IS NULL)        AS NULL_STATUS,
    SUM(SENDER_AGE_GROUP IS NULL)          AS NULL_SENDER_AGE,
    SUM(RECEIVER_AGE_GROUP IS NULL)        AS NULL_RECEIVER_AGE,
    SUM(SENDER_STATE IS NULL)              AS NULL_STATE,
    SUM(SENDER_BANK IS NULL)               AS NULL_SENDER_BANK,
    SUM(RECEIVER_BANK IS NULL)             AS NULL_RECEIVER_BANK,
    SUM(DEVICE_TYPE IS NULL)               AS NULL_DEVICE,
    SUM(NETWORK_TYPE IS NULL)              AS NULL_NETWORK,
    SUM(FRAUD_FLAG IS NULL)                AS NULL_FRAUD_FLAG,
    SUM(HOUR_OF_DAY IS NULL)               AS NULL_HOUR,
    SUM(DAY_OF_WEEK IS NULL)               AS NULL_DAY_OF_WEEK
FROM UPI_TRANSACTIONS;

-- Q12. CHECK DUPLICATE TRANSACTION IDS
SELECT `TRANSACTION ID`, COUNT(*) AS OCCURRENCES
FROM UPI_TRANSACTIONS
GROUP BY `TRANSACTION ID`
HAVING COUNT(*) > 1;

-- Q13. CHECK DUPLICATE COMPLETE RECORDS 
WITH RANKED AS (
    SELECT `TRANSACTION ID`,
           ROW_NUMBER() OVER (
               PARTITION BY `TIMESTAMP`, `TRANSACTION TYPE`, MERCHANT_CATEGORY,
                            `AMOUNT (INR)`, TRANSACTION_STATUS, SENDER_BANK,
                            RECEIVER_BANK, SENDER_STATE
               ORDER BY `TRANSACTION ID`
           ) AS RN
    FROM UPI_TRANSACTIONS
)
SELECT COUNT(*) AS FULL_DUPLICATE_ROWS
FROM RANKED
WHERE RN > 1;

-- Q14. CHECK NEGATIVE TRANSACTION AMOUNTS
SELECT COUNT(*) AS NEGATIVE_AMOUNT_ROWS
FROM UPI_TRANSACTIONS
WHERE `AMOUNT (INR)` < 0;

-- Q15. CHECK ZERO-VALUE TRANSACTIONS
SELECT COUNT(*) AS ZERO_AMOUNT_ROWS
FROM UPI_TRANSACTIONS
WHERE `AMOUNT (INR)` = 0;

-- Q16. CHECK INVALID HOUR VALUES (MUST BE 0–23)
SELECT COUNT(*) AS INVALID_HOUR_ROWS
FROM UPI_TRANSACTIONS
WHERE HOUR_OF_DAY NOT BETWEEN 0 AND 23;

-- Q17. CHECK INVALID FRAUD FLAG VALUES (MUST BE 0 OR 1)
SELECT COUNT(*) AS INVALID_FRAUD_FLAG_ROWS
FROM UPI_TRANSACTIONS
WHERE FRAUD_FLAG NOT IN (0, 1);

-- Q18. CHECK BLANK MERCHANT CATEGORIES
SELECT COUNT(*) AS BLANK_MERCHANT_CATEGORY_ROWS
FROM UPI_TRANSACTIONS
WHERE MERCHANT_CATEGORY IS NULL OR TRIM(MERCHANT_CATEGORY) = '';

